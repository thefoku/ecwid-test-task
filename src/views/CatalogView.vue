<script setup lang="ts">
import Button from 'primevue/button'
import { useCategories } from '@/services/categoryService'
import { onMounted, watch } from 'vue'
import { useCategoryStore } from '@/store/useCategoryStore'
import CategoryCard from '@/components/CategoryPage/CategoryCard.vue'
import BreadCrumbs from '@/components/CategoryPage/BreadCrumbs.vue'
import { useRoute } from 'vue-router'

const { fetchCategories } = useCategories()
const categoryStore = useCategoryStore()

const route = useRoute()

watch(
  () => route.params.categorySlug,
  (newSlug) => {
    categoryStore.categories = categoryStore.getCategoriesByParentId(newSlug as string) ?? [] // maybe search by slug?
  },
)

onMounted(async () => {
  await categoryStore.loadCategories()
  if (route.params.categorySlug) {
    categoryStore.categories =
      categoryStore.getCategoriesByParentId(route.params.categorySlug as string) ?? []
  }
})
</script>

<template>
  <main>
    <h1 class="text-center text-2xl font-bold mb-4">Catalog</h1>
    <BreadCrumbs />
    <div class="category-list">
      <CategoryCard
        v-for="category in categoryStore.categories"
        :key="category.id"
        :image="category.image || ''"
        :name="category.name"
        :slug="category.autogeneratedSlug"
      />
    </div>
    <Button
      label="Fetch Categories"
      outlined
      severity="secondary"
      @click="fetchCategories(173495753)"
    />
    <Button
      label="All Categories"
      outlined
      severity="secondary"
      @click="console.log(categoryStore.allCategories)"
    />
  </main>
</template>

